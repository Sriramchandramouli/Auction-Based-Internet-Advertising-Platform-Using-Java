            if(supplierOrganization ==null) {
                JOptionPane.showMessageDialog(null, "Please assign organization to suplier before creating");
                return;
            }
        String supplierName = namejTextField.getText();
        Supplier supplier = supplierDirectory.addSupplier();
        supplier.setFirstName(supplierName);
        namejTextField.setText("");
        
        JOptionPane.showMessageDialog(null, "Supplier created successfully", "Success", JOptionPane.WARNING_MESSAGE);
