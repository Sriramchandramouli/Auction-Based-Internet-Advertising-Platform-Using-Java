    private Boolean validation() {
        if(namejTextField.getText().equals("")) {
         JOptionPane.showMessageDialog(null, "Fill in the supplier field. It should not be empty", "Empty Text", JOptionPane.WARNING_MESSAGE);
         return false;   
        }
        String supName = namejTextField.getText();
        for(int i = 0; i <supName.length();i++){
        char c = supName.charAt(i);  
        if((c<97 || c>122) && (c<65 || c>90)) {
         JOptionPane.showMessageDialog(null, "Supplier Name field should contain only alphabet", "Mismatch", JOptionPane.WARNING_MESSAGE);
         return false;
        }
        }
        return true;
    }

