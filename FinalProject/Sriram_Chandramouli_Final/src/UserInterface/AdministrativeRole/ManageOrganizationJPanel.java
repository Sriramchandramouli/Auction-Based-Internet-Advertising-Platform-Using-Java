    private void createOrganizationJButtonActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_createOrganizationJButtonActionPerformed
       Type type = (Type)orgJComboBox.getSelectedItem();
       for(Organization org : organizationDirectory.getOrganizationList()) {
           if(type.getValue().equals(org.getName())) {
               
               JOptionPane.showMessageDialog(null, "Organization already exists");
               return; 
           }
       }
       organizationDirectory.createOrganization(type);
       populateTable();
    }//GEN-LAST:event_createOrganizationJButtonActionPerformed

    private void deleteOrgJButtonActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_deleteOrgJButtonActionPerformed
        int selectedRow = organizationJTable.getSelectedRow();
        if(selectedRow>=0){
            Organization org  =(Organization)organizationJTable.getValueAt(selectedRow,1);
                if (org.getName().equals("Admin Organization")) {
                    JOptionPane.showMessageDialog(null, "You do not have an access to delete Admin Organization");
                    return;
                }
            int option = JOptionPane.showConfirmDialog(null, "Are you sure you want to delete", "Warning", JOptionPane.YES_NO_OPTION);
            if (option == JOptionPane.YES_OPTION) {
                
                organizationDirectory.deleteOrganization(org);
                populateTable();
            }
        }
        else{
            JOptionPane.showMessageDialog(this,"Please select an Organization to delete");
        }
    }//GEN-LAST:event_deleteOrgJButtonActionPerformed


