    private void populateCombobox() {
        orgJComboBox.removeAllItems();
        for(Type type : Organization.Type.values()) {
        
        if ((!type.getValue().equals(Type.Admin.getValue())) && (!type.getValue().equals(Type.Consumer.getValue()))){
            orgJComboBox.addItem(type);
        }
        }
    }
    private void populateTable() {
        
        DefaultTableModel dtm = (DefaultTableModel)organizationJTable.getModel();
        dtm.setRowCount(0);
        
        for(Organization organization : organizationDirectory.getOrganizationList()) {
            Object[] row = new Object[2];
            row[0] = organization.getOrganizationID();
            row[1] = organization;
            dtm.addRow(row);
        }
    }

