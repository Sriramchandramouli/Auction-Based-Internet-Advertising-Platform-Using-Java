    private void populateAdvertiserTable() {
        DefaultTableModel dtm = (DefaultTableModel)advertiserJTable.getModel();
        dtm.setRowCount(0);
        
        for (Advertiser advertiser : advertiserDirectory.getAdvertiserList()) {
            Object row[] = new Object[1];
            row[0] = advertiser;
            dtm.addRow(row);
        }
    }

