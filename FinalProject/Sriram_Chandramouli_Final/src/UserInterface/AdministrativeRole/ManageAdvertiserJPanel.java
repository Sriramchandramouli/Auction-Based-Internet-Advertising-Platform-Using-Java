    private void removeJButtonActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_removeJButtonActionPerformed
        int selectedRow = advertiserJTable.getSelectedRow();
        if(selectedRow>=0){
            int option = JOptionPane.showConfirmDialog(null, "Are you sure you want to delete", "Warning", JOptionPane.YES_NO_OPTION);
            if (option == JOptionPane.YES_OPTION){
                Advertiser advertiser = (Advertiser)advertiserJTable.getValueAt(selectedRow,0);
                advertiserDirectory.deleteAdvertiser(advertiser);
                populateAdvertiserTable();
            }
        }
        else{
            JOptionPane.showMessageDialog(this,"Please select an Advertiser to delete");
        }
    }//GEN-LAST:event_removeJButtonActionPerformed

