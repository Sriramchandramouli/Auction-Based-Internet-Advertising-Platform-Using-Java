    private void populateProductCombobox() {
        productJComboBox.removeAllItems();
        for(Product product : advertiser.getProductCatalog().getProductList()) {
            productJComboBox.addItem(product);
        }
    }
    
    private Boolean adSpacevalidation() {
        if(adNameField.getText().equals("") || adTextField.getText().equals("")) {
            JOptionPane.showMessageDialog(null, "Fill in all the fields", "Empty Text", JOptionPane.WARNING_MESSAGE);
         return false;
        }
        Product product = (Product) productJComboBox.getSelectedItem();
        if (product == null) {
                JOptionPane.showMessageDialog(null, "Please create a product for advertising");
                return false;
            } 
        String name = adNameField.getText();
        for(int i = 0; i <name.length();i++){
        char c = name.charAt(i);  
        if((c<97 || c>122) && (c<65 || c>90)) {
         JOptionPane.showMessageDialog(null, "Name field should contain only alphabet", "Mismatch", JOptionPane.WARNING_MESSAGE);
         return false;
   
        }
        }
        String text = adTextField.getText();
        for(int i = 0; i <text.length();i++){
        char c = text.charAt(i);  
        if((c<97 || c>122) && (c<65 || c>90)) {
         JOptionPane.showMessageDialog(null, "Text field should contain only alphabet", "Mismatch", JOptionPane.WARNING_MESSAGE);
         return false;
        }
        }
        return true;
    }
    
    

