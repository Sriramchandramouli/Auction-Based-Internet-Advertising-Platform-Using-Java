        if(flag) {
            Advertisement advertisement = advertiser.getAdvertisementCatalog().addAdvertisement();
            String adName = adNameField.getText();
            String adText = adTextField.getText();
            advertisement.setName(adName);
            advertisement.setAd(adText);
            Product product = (Product) productJComboBox.getSelectedItem();
            
            
            advertisement.setProduct(product);
       //     advertisement.getProduct().setProductImage(link);
          
            adNameField.setText("");
            adTextField.setText("");
            JOptionPane.showMessageDialog(null, "Advertisement created successfully", "Success", JOptionPane.WARNING_MESSAGE);
        }
