    private void deleteadvertisementJButtonActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_deleteadvertisementJButtonActionPerformed
        int selectedRow = advertisementCatalog.getSelectedRow();
        if(selectedRow>=0){
            int option = JOptionPane.showConfirmDialog(null, "Are you sure you want to delete", "Warning", JOptionPane.YES_NO_OPTION);
            if (option == JOptionPane.YES_OPTION) {
                Advertisement advertisement  =(Advertisement)advertisementCatalog.getValueAt(selectedRow,0);
                advertiser.getAdvertisementCatalog().removeAdvertisement(advertisement);
                populateAdvertisementTable();
            }
        }
        else{
            JOptionPane.showMessageDialog(this,"Please select an advertisement to delete");
        }
    }//GEN-LAST:event_deleteadvertisementJButtonActionPerformed

