    private void populateAdvertisementTable(){
    DefaultTableModel dtm = (DefaultTableModel)advertisementCatalog.getModel();
    dtm.setRowCount(0); 
    
    for(Advertisement advertisement : advertiser.getAdvertisementCatalog().getAdvertisementList()){
        Object row[]= new Object[2];
        row[0] = advertisement;
        row[1] = advertisement.getProduct().getCategory();
        
        dtm.addRow(row);
    }
}

